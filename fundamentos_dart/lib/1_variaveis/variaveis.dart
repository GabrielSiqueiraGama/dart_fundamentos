void main() {
  //inteiros :D numeros inteiros, sem quebra de numero.
  int idade = 18;
  print(idade);
  //double :D numeros quebrados, numeros com virgular.
  double valor = 3.50;
  print(valor);
  //num :D aceita todo tipo de numero.
  num numero = 1.2;
  numero = 1;
  print(numero);
  //string :D texto meu chapa
  String nome = 'Gabriel Siqueira Gama';
  print(nome);
  //var :D a propria linguagem interpreta qual o tipo da variavel
  var cpf = 123456789876543;
  print(cpf);
  //dynamic :D aceita alterar o tipo da variavel
  dynamic qualquercoisa = 1234;
  qualquercoisa = 'aoba';
  print(qualquercoisa);
}
